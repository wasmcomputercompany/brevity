  @Test
  fun `full world`() {
    val ioPackage = IoWitPackage(
      packageName = "wasi:cli@0.3.0".toPackageName(),
      files = mapOf(
        "command.wit".toPath() to """
          |package wasi:cli@0.3.0;
          |
          |world command {
          |  include imports;
          |  export run;
          |  export args: func() -> list<string>;
          |  import platform: interface {
          |    log: func(message: string);
          |  }
          |}
          """.trimMargin().toWitFile(),
        "exit.wit".toPath() to """
          |interface exit {
          |  exit: func(status: result);
          |}
          """.trimMargin().toWitFile(),
        "imports.wit".toPath() to """
          |package wasi:cli@0.3.0;
          |
          |world imports {
          |  import exit;
          |}
          """.trimMargin().toWitFile(),
        "run.wit".toPath() to """
          |interface run {
          |  run: async func() -> result;
          |}
          """.trimMargin().toWitFile(),
      ),
    )

    val irPackage = IrMapper(listOf(ioPackage)).map().single()
    val ktPackage = KtMapper().map(irPackage)
    val fileSpec = ApiGenerator().generate(
      witPackage = ktPackage,
      worldFilter = { world -> world.type.simpleName == "Command" },
    )

    assertThat(fileSpec.toString()).isEqualTo(
      """
      |// Generated by Brevity. Do not edit.
      |package wit.wasi.cli.v0_3_0
      |
      |import kotlin.Pair
      |import kotlin.String
      |import kotlin.collections.List
      |
      |public object Command {
      |  public interface Guest {
      |    public val run: Run
      |
      |    public fun args(): List<String>
      |  }
      |
      |  public interface Host {
      |    public val platform: Platform
      |
      |    public val exit: Exit
      |
      |    public interface Platform {
      |      public fun log(message: String)
      |    }
      |  }
      |}
      |
      |public interface Exit {
      |  public fun exit(status: Pair<*, *>)
      |}
      |
      |public interface Run {
      |  public fun run(): Pair<*, *>
      |}
      |
      """.trimMargin(),
    )
  }

  /** We had a bug where we used the wrong package name when following includes across packages. */
  @Test
  fun `imports across packages`() {
    val cliPackage = IoWitPackage(
      packageName = "wasi:cli@0.3.0".toPackageName(),
      files = mapOf(
        "command.wit".toPath() to """
          |package wasi:cli@0.3.0;
          |
          |world command {
          |  import wasi:clocks/monotonic-clock@0.3.0;
          |}
          """.trimMargin().toWitFile(),
      ),
    )
    val clockPackage = IoWitPackage(
      packageName = "wasi:clocks@0.3.0".toPackageName(),
      files = mapOf(
        "clock.wit".toPath() to """
          |package wasi:clocks@0.3.0;
          |
          |interface monotonic-clock {
          |  now: func() -> s64;
          |}
          """.trimMargin().toWitFile(),
      ),
    )

    val irPackages = IrMapper(listOf(cliPackage, clockPackage)).map()
    val ktPackages = irPackages.map(KtMapper()::map)
    val fileSpec = ApiGenerator().generate(
      witPackage = ktPackages.single { it.packageName == "wit.wasi.cli.v0_3_0" },
    )

    assertThat(fileSpec.toString()).isEqualTo(
      """
      |// Generated by Brevity. Do not edit.
      |package wit.wasi.cli.v0_3_0
      |
      |import wit.wasi.clocks.v0_3_0.MonotonicClock
      |
      |public object Command {
      |  public interface Host {
      |    public val monotonicClock: MonotonicClock
      |  }
      |}
      |
      """.trimMargin(),
    )
  }

  /**
   * Don't declare a property for imported interfaces that don't declare any functions. Otherwise,
   * we may get a collision declaring multiple mutually-redundant `types` members.
   */
  @Test
  fun `imports of interfaces without functions are skipped`() {
    val socketsPackage = IoWitPackage(
      packageName = "wasi:sockets@0.3.0".toPackageName(),
      files = mapOf(
        "sockets.wit".toPath() to """
          |package wasi:sockets@0.3.0;
          |
          |@since(version = 0.3.0)
          |world imports {
          |    @since(version = 0.3.0)
          |    import types;
          |}
          """.trimMargin().toWitFile(),
        "types.wit".toPath() to """
          |interface types {
          |    type ipv4-address = tuple<u8, u8, u8, u8>;
          |    type ipv6-address = tuple<u16, u16, u16, u16, u16, u16, u16, u16>;
          |
          |    variant ip-address {
          |        ipv4(ipv4-address),
          |        ipv6(ipv6-address),
          |    }
          |}
          """.trimMargin().toWitFile(),
      ),
    )

    val irPackages = IrMapper(listOf(socketsPackage)).map()
    val ktPackages = irPackages.map(KtMapper()::map)
    val fileSpec = ApiGenerator().generate(ktPackages.single())

    assertThat(fileSpec.toString()).isEqualTo(
      """
      |// Generated by Brevity. Do not edit.
      |package wit.wasi.sockets.v0_3_0
      |
      |import dev.wasmo.brevity.Quad
      |import kotlin.UByte
      |import kotlin.collections.List
      |import kotlin.jvm.JvmInline
      |
      |public object Imports
      |
      |public interface Types {
      |  @JvmInline
      |  public value class Ipv4Address(
      |    public val `value`: Quad<UByte, UByte, UByte, UByte>,
      |  )
      |
      |  @JvmInline
      |  public value class Ipv6Address(
      |    public val `value`: List<*>,
      |  )
      |
      |  public sealed interface IpAddress {
      |    public data class Ipv4(
      |      public val `value`: Ipv4Address,
      |    ) : IpAddress
      |
      |    public data class Ipv6(
      |      public val `value`: Ipv6Address,
      |    ) : IpAddress
      |  }
      |}
      |
      """.trimMargin(),
    )
  }
}
