package dev.wasmo.brevity.kotlin.generator

import com.squareup.kotlinpoet.AnnotationSpec
import com.squareup.kotlinpoet.CodeBlock
import com.squareup.kotlinpoet.FileSpec
import com.squareup.kotlinpoet.FunSpec
import com.squareup.kotlinpoet.INT
import com.squareup.kotlinpoet.KModifier
import com.squareup.kotlinpoet.MemberName
import com.squareup.kotlinpoet.ParameterSpec
import com.squareup.kotlinpoet.PropertySpec
import com.squareup.kotlinpoet.TypeSpec
import com.squareup.kotlinpoet.UNIT

class GuestGenerator(
  private val packages: List<KtWitPackage>,
) {
  private val guestBridge = CodeBlock.of(
    "%M",
    MemberName(
      Symbols.Brevity.GuestBridge.packageName,
      Symbols.Brevity.GuestBridge.simpleName,
    ),
  )

  fun generate(witPackage: KtWitPackage): FileSpec {
    return FileSpec.builder(witPackage.packageName, "Guest")
      .addFileComment("Generated by Brevity. Do not edit.")
      .apply {
        for (declaration in witPackage.items) {
          addPackageItem(declaration)
        }
      }
      .build()
  }

  private fun FileSpec.Builder.addPackageItem(
    value: KtWitPackage.Item,
  ) {
    when (value) {
      is KtInterface -> addInterface(value)
      is KtWorld -> addWorld(value)
    }
  }

  private fun FileSpec.Builder.addInterface(value: KtInterface) {
    for (item in value.items) {
      addInterfaceItem(item)
    }
  }

  private fun FileSpec.Builder.addInterfaceItem(value: KtInterface.Item) {
    when (value) {
      is KtResource -> addType(resourceToGuestBridge(value))
      else -> {}
    }
  }

  private fun FileSpec.Builder.addWorld(
    value: KtWorld,
  ) {
    if (value.guest.apis.isEmpty()) return

    val guestFieldName = "${value.type.simpleName}_${value.guest.name}"

    addProperty(
      PropertySpec.builder(guestFieldName, value.guest.type)
        .addModifiers(KModifier.PRIVATE, KModifier.LATEINIT)
        .mutable(true)
        .build(),
    )

    addProperty(
      PropertySpec.builder(value.guest.name, value.guest.type)
        .receiver(value.type)
        .mutable(true)
        .getter(
          FunSpec.getterBuilder()
            .addCode("return $guestFieldName")
            .build(),
        )
        .setter(
          FunSpec.setterBuilder()
            .addParameter("value", value.guest.type)
            .addCode("%N = %N", guestFieldName, "value")
            .build(),
        )
        .build(),
    )

    for (api in value.guest.apis) {
      when (api) {
        is KtExternalApi -> {
          for (function in api.functions) {
            addFunction(
              functionToGuest(
                guestFieldName = guestFieldName,
                parentName = api.name,
                value = function,
              ),
            )
          }
        }

        is KtFunction -> addFunction(
          functionToGuest(
            guestFieldName = guestFieldName,
            parentName = null,
            value = api,
          ),
        )

        is KtInterface -> {}
      }
    }

    for (item in value.items) {
      when (item) {
        is KtResource -> addType(resourceToGuestBridge(item))
        else -> {}
      }
    }
  }

  private fun functionToGuest(
    guestFieldName: String,
    parentName: String? = null,
    value: KtFunction,
  ): FunSpec {
    return FunSpec.builder("${guestFieldName}_${value.ktName}")
      .addModifiers(KModifier.PRIVATE)
      .addAnnotation(
        AnnotationSpec.builder(Symbols.KotlinWasm.WasmExport)
          .addMember("%S", value.name)
          .build(),
      )
      .apply {
        if (value.returnType != null) {
          addCode("return ")
          returns(value.returnType.abiType)
        }
        addCode("%N.", guestFieldName)
        if (parentName != null) {
          addCode("%N.", parentName)
        }
        addCode("%N(", value.ktName)
        for ((index, parameter) in value.parameters.withIndex()) {
          if (index > 0) addCode(", ")
          addParameter(ParameterSpec.builder(parameter.name, parameter.type.abiType).build())
          addCode(guestAbiToApi(guestBridge, parameter.type, CodeBlock.of("%N", parameter.name)))
        }
        addCode(")")
        if (value.returnType != null) {
          addCode(" as %T", value.returnType.abiType)
        }
        addCode("\n")
      }

      .build()
  }

  private fun resourceToGuestBridge(value: KtResource): TypeSpec {
    return TypeSpec.classBuilder(bridgedType(value.type))
      .addSuperinterface(value.type)
      .primaryConstructor(
        FunSpec.constructorBuilder()
          .addParameter(ParameterSpec.builder("id", INT).build())
          .build(),
      )
      .apply {
        for (function in value.functions) {
          addFunction(resourceFunctionToGuestBridge(function))
        }
      }
      .build()
  }

  private fun resourceFunctionToGuestBridge(value: KtFunction) = FunSpec.builder(value.ktName)
    .addModifiers(KModifier.OVERRIDE)
    .apply {
      for (parameter in value.parameters) {
        addParameter(parameter.name, parameter.type.apiType)
      }
      returns(value.returnType?.apiType ?: UNIT)
    }
    .addCode("error(%S)", "TODO")
    .build()
}
