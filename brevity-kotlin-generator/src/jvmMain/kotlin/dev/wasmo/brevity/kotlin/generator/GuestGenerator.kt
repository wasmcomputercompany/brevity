package dev.wasmo.brevity.kotlin.generator


import com.squareup.kotlinpoet.CodeBlock
import com.squareup.kotlinpoet.FileSpec
import com.squareup.kotlinpoet.FunSpec
import com.squareup.kotlinpoet.INT
import com.squareup.kotlinpoet.KModifier
import com.squareup.kotlinpoet.PropertySpec
import com.squareup.kotlinpoet.TypeSpec
import dev.wasmo.brevity.DeclarationIndex
import dev.wasmo.brevity.RoleTracker
import dev.wasmo.brevity.TypeName
import dev.wasmo.brevity.ir.IrExternalApi
import dev.wasmo.brevity.ir.IrFunction
import dev.wasmo.brevity.ir.IrInterface
import dev.wasmo.brevity.ir.IrResource
import dev.wasmo.brevity.ir.IrWitPackage
import dev.wasmo.brevity.ir.IrWorld
import dev.wasmo.brevity.kotlin.generator.GuestBridgeBuilder.Receiver

class GuestGenerator(
  private val declarationIndex: DeclarationIndex,
  private val roleTracker: RoleTracker,
  private val packages: List<IrWitPackage>,
) {
  private val bridgeBuilder = GuestBridgeBuilder(declarationIndex)

  fun generate(): List<FileSpec> {
    val bridges = roleTracker.types.entries
      .groupBy { (key, _) -> key.serviceName.packageName.toKotlin().name }
      .mapNotNull { (`package`, entries) ->
        FileSpec.builder(`package`, "GuestTypes")
          .addFileComment("Generated by Brevity. Do not edit.")
          .apply {
            for ((key, value) in entries) {
              addCodec(key, value)
            }
          }
          .build()
          .takeIf { it.members.isNotEmpty() }
      }

    val services = packages.mapNotNull { witPackage ->
        FileSpec.builder(witPackage.packageName.toKotlin().name, "GuestServices")
          .addFileComment("Generated by Brevity. Do not edit.")
          .apply {
            for (service in witPackage.services) {
              generateService(service)

              if (service is IrWorld) {
                addExternalFunctions(service)
              }
            }
          }
          .build()
          .takeIf { it.members.isNotEmpty() }
      }

    return bridges + services
  }

  private fun FileSpec.Builder.generateService(value: IrWitPackage.Service) {
    if (value is IrWorld) {
      val guestApis = value.guestApis
      if (guestApis != null) {
        addProperty(
          PropertySpec.builder("${guestApis.instanceName}_", guestApis.type)
            .addModifiers(KModifier.PRIVATE, KModifier.LATEINIT)
            .mutable(true)
            .build(),
        )
        addProperty(
          PropertySpec.builder(guestApis.instanceName, guestApis.type)
            .receiver(value.serviceName.kotlinApi)
            .mutable(true)
            .getter(
              FunSpec.getterBuilder()
                .addCode("return %N", "${guestApis.instanceName}_")
                .build(),
            )
            .setter(
              FunSpec.setterBuilder()
                .addParameter("value", guestApis.type)
                .addCode("%N = %N", "${guestApis.instanceName}_", "value")
                .build(),
            )
            .build(),
        )
      }
    }
  }

  private fun FileSpec.Builder.addCodec(
    typeName: TypeName.Declared,
    entry: RoleTracker.Entry,
  ) {
    when (val typeDeclaration = declarationIndex[typeName]) {
      is IrResource -> {
        addResource(
          value = typeDeclaration,
          host = entry.host,
          guest = entry.guest,
        )
      }

      else -> {} // TODO
    }
  }

  private fun FileSpec.Builder.addResource(
    value: IrResource,
    host: Boolean,
    guest: Boolean,
  ) {
    val receiver = Receiver.Id(
      type = value.type,
    )

    if (guest) {
      for (function in value.functions) {
        if (!function.isSupported) continue // TODO
        addFunction(bridgeBuilder.wasmExportFunction(receiver, function))
      }
    }

    if (host) {
      val handleBuilder = TypeSpec.classBuilder(value.type.handleName)
        .addModifiers(KModifier.INTERNAL)
        .addSuperinterface(value.type.kotlinApi)
        .primaryConstructor(
          FunSpec.constructorBuilder()
            .addParameter("id", INT)
            .build(),
        )
        .addProperty(
          PropertySpec.builder("id", INT)
            .addModifiers(KModifier.PRIVATE)
            .initializer("id")
            .build(),
        )

      for (function in value.functions) {
        if (!function.isSupported) continue // TODO
        handleBuilder.addFunction(bridgeBuilder.callHostFunction(receiver, function))
        addFunction(bridgeBuilder.wasmImportFunction(receiver, function))
      }

      addType(handleBuilder.build())
    }
  }

  /**
   * Generate top-level `@WasmExport`-annotated functions for all exported functions in [value],
   * and functions recursively held by [value].
   */
  private fun FileSpec.Builder.addExternalFunctions(value: IrWorld) {
    // The object to dereference that defines the true implementation. This is either the guest
    // interface or one of its members.
    val guestApis = value.guestApis ?: return

    for (item in guestApis.items) {
      when (item) {
        is IrFunction -> {
          val receiver = Receiver.Global(
            CodeBlock.of("%N_", guestApis.instanceName),
          )
          addFunction(
            bridgeBuilder.wasmExportFunction(
              receiver = receiver,
              value = item,
            ),
          )
        }

        is IrExternalApi -> {
          val irInterface = declarationIndex[item.serviceName] as IrInterface
          val receiver = Receiver.Global(
            CodeBlock.of("%N_.%N", guestApis.instanceName, item.instanceName),
          )
          for (function in irInterface.functions) {
            addFunction(
              bridgeBuilder.wasmExportFunction(
                receiver = receiver,
                value = function,
              ),
            )
          }
        }
      }
    }
  }
}
