      "types.wit".toPath() to """
        |interface types {
        |    type ipv4-address = tuple<u8, u8, u8, u8>;
        |    type ipv6-address = tuple<u16, u16, u16, u16, u16, u16, u16, u16>;
        |
        |    variant ip-address {
        |        ipv4(ipv4-address),
        |        ipv6(ipv6-address),
        |    }
        |}
        """.trimMargin(),
    )

    assertThat(tester["wit/wasi/sockets/v0_3_0/Api.kt".toPath()]).isEqualTo(
